                                        .id("string")
                                        .object_("string")
                                        .bytes(123L)
                                        .createdAt(123L)
                                        .filename("string")
                                        .purpose("string")
                                        .status("string")
                                        .statusDetails(JsonString.of("abc"))
                                        .build()
                                )
