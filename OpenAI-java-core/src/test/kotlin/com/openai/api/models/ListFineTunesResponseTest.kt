                            .events(
                                listOf(
                                    FineTune.FineTuneEvent.builder()
                                        .object_("string")
                                        .createdAt(123L)
                                        .level("string")
                                        .message("string")
                                        .build()
                                )
                            )
                            .build()
                    )
                )
                .build()
        assertThat(listFineTunesResponse).isNotNull
        assertThat(listFineTunesResponse.object_()).isEqualTo("string")
        assertThat(listFineTunesResponse.data())
            .containsExactly(
                FineTune.builder()
