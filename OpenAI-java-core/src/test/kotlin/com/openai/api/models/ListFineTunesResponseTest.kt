                    .object_("string")
                    .createdAt(123L)
                    .updatedAt(123L)
                    .model("string")
                    .fineTunedModel("string")
                    .organizationId("string")
                    .status("string")
                    .hyperparams(JsonString.of("abc"))
                    .trainingFiles(
                        listOf(
                            File.builder()
